export function t(key: string, lang: Language = 'en', params?: Record<string, string | number>): string {
  let text = translations[lang]?.[key] || translations.en[key] || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  return text;
}

export function getLocalizedField<T extends Record<string, any>>(item: T, field: string, lang: Language): string {
  const localizedKey = `${field}_${lang}`;
  const fallbackKey = `${field}_en`;
  return (item[localizedKey] as string) || (item[fallbackKey] as string) || '';
}
